USE ROLE ACCOUNTADMIN;

-- ============================================================
-- 1. DBT ROLE
-- ============================================================

CREATE ROLE IF NOT EXISTS DBT_ROLE
    COMMENT = 'Role used by dbt and Airflow Cosmos';

-- Warehouse access

GRANT USAGE
ON WAREHOUSE TRIPLENS_WH
TO ROLE DBT_ROLE;

-- Database access

GRANT USAGE
ON DATABASE TRIPLENS
TO ROLE DBT_ROLE;

-- RAW schema access

GRANT USAGE
ON SCHEMA TRIPLENS.RAW
TO ROLE DBT_ROLE;

GRANT SELECT
ON ALL TABLES IN SCHEMA TRIPLENS.RAW
TO ROLE DBT_ROLE;

GRANT SELECT
ON FUTURE TABLES IN SCHEMA TRIPLENS.RAW
TO ROLE DBT_ROLE;

-- BRONZE schema access

GRANT USAGE
ON SCHEMA TRIPLENS.BRONZE
TO ROLE DBT_ROLE;

GRANT CREATE TABLE, CREATE VIEW
ON SCHEMA TRIPLENS.BRONZE
TO ROLE DBT_ROLE;

GRANT SELECT, INSERT, UPDATE, DELETE, TRUNCATE
ON ALL TABLES IN SCHEMA TRIPLENS.BRONZE
TO ROLE DBT_ROLE;

GRANT SELECT
ON ALL VIEWS IN SCHEMA TRIPLENS.BRONZE
TO ROLE DBT_ROLE;

GRANT SELECT
ON FUTURE TABLES IN SCHEMA TRIPLENS.BRONZE
TO ROLE DBT_ROLE;

GRANT SELECT
ON FUTURE VIEWS IN SCHEMA TRIPLENS.BRONZE
TO ROLE DBT_ROLE;

-- SILVER schema access

GRANT USAGE
ON SCHEMA TRIPLENS.SILVER
TO ROLE DBT_ROLE;

GRANT CREATE TABLE, CREATE VIEW
ON SCHEMA TRIPLENS.SILVER
TO ROLE DBT_ROLE;

GRANT SELECT, INSERT, UPDATE, DELETE, TRUNCATE
ON ALL TABLES IN SCHEMA TRIPLENS.SILVER
TO ROLE DBT_ROLE;

GRANT SELECT
ON ALL VIEWS IN SCHEMA TRIPLENS.SILVER
TO ROLE DBT_ROLE;

GRANT SELECT
ON FUTURE TABLES IN SCHEMA TRIPLENS.SILVER
TO ROLE DBT_ROLE;

GRANT SELECT
ON FUTURE VIEWS IN SCHEMA TRIPLENS.SILVER
TO ROLE DBT_ROLE;

-- GOLD schema access

GRANT USAGE
ON SCHEMA TRIPLENS.GOLD
TO ROLE DBT_ROLE;

GRANT CREATE TABLE, CREATE VIEW
ON SCHEMA TRIPLENS.GOLD
TO ROLE DBT_ROLE;

GRANT SELECT, INSERT, UPDATE, DELETE, TRUNCATE
ON ALL TABLES IN SCHEMA TRIPLENS.GOLD
TO ROLE DBT_ROLE;

GRANT SELECT
ON ALL VIEWS IN SCHEMA TRIPLENS.GOLD
TO ROLE DBT_ROLE;

GRANT SELECT
ON FUTURE TABLES IN SCHEMA TRIPLENS.GOLD
TO ROLE DBT_ROLE;

GRANT SELECT
ON FUTURE VIEWS IN SCHEMA TRIPLENS.GOLD
TO ROLE DBT_ROLE;

-- ANALYTICS schema access

GRANT USAGE
ON SCHEMA TRIPLENS.ANALYTICS
TO ROLE DBT_ROLE;

